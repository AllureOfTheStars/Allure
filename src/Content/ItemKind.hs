  { isymbol  = '"'
  , iflavour = [(BrGreen, True)]
  , iname    = "necklace"
  , ieffect  = Regeneration
  , icount   = intToQuad 1
  , ifreq    = 10
  , ipower   = (RollDice 2 1, RollDice 2 2)
  , iverbApply   = "tear down"
  , iverbProject = "throw"
  }
